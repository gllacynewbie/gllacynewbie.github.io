$('.forDrop').click (function () {
    		if($('.dropdown').is(':visible'))
    		   $('.dropdown').hide ();
    		else 
    			$('.dropdown').show ();
});

$('.searchDrop').click (function () {
    		if($('.hiddenSearch').is(':visible'))
    		   $('.hiddenSearch').hide ();
    		else 
    			$('.hiddenSearch').show ();
});

$('.entry').click (function () {
    		if($('.loginForm').is(':visible'))
    		   $('.loginForm').hide ();
    		else 
    			$('.loginForm').show ();
});